package com.bq.java.oops.encapsulation;

public class Encaps 
{	
	private String name;
	private String mobile;
	
	public String getName() 
	{
		return name;
	}
	public void setName(String name) 
	{
		this.name = name;
	}
	
	
	public String getMobile() 
	{
		return mobile;
	}
	public void setMobile(String mobile) 
	{
		this.mobile = mobile;
	}
	
	

}
