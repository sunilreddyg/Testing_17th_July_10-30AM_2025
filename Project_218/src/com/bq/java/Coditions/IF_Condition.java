package com.bq.java.Coditions;

public class IF_Condition {

	public static void main(String[] args) {
		
		
		

		//If is Single Decision Statement
		//IF accept only boolean condition..
		
		System.out.println("Statement1");
		
		//On True Condition Statement2 will be printed
		if (true) 
		{
			System.out.println("Statement2");
			System.out.println("True Condition accepted");
		}
		
		System.out.println("Statement3");
		
		
		
		if(5 > 4)
			System.out.println("accepted condition");
		
		
		if("hi".equals("hi"))
			System.out.println("accepted condition");
		
		System.out.println("Final Statement");

	}

}
